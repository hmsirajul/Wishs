let activeMemoryIndex = 0;
let activeSlideIndex = 0;
let memoryTimer = null;

function updateMemorySlide(){
    const viewer = document.getElementById("memoryViewer");
    const image = document.getElementById("viewerImage");
    const title = document.getElementById("viewerTitle");
    const caption = document.getElementById("viewerCaption");
    const memory = memories[activeMemoryIndex];
    const slide = memory.slides[activeSlideIndex];

    title.textContent = memory.title;
    image.src = slide.src;
    image.alt = `${memory.title} slide ${activeSlideIndex + 1}`;
    caption.textContent = slide.caption;
}

function startMemoryTimer(){
    stopMemoryTimer();
    memoryTimer = setInterval(nextSlide, 300000); // 5 minutes
}

function stopMemoryTimer(){
    if(memoryTimer){
        clearInterval(memoryTimer);
        memoryTimer = null;
    }
}

function openMemoryViewer(index){
    activeMemoryIndex = index;
    activeSlideIndex = 0;
    updateMemorySlide();
    document.getElementById("memoryViewer").classList.remove("hidden");
    document.getElementById("memoryViewer").setAttribute("aria-hidden", "false");
    startMemoryTimer();
    startFireworks();
    startBalloons();
}

function closeMemoryViewer(){
    stopMemoryTimer();
    document.getElementById("memoryViewer").classList.add("hidden");
    document.getElementById("memoryViewer").setAttribute("aria-hidden", "true");
    stopFireworks();
    stopBalloons();
    // if the gallery was open before we showed the viewer, restore it
    if(window._wasGalleryOpen){
        openPhotoGallery();
        window._wasGalleryOpen = false;
    }
}

function nextSlide(){
    const memory = memories[activeMemoryIndex];
    activeSlideIndex = (activeSlideIndex + 1) % memory.slides.length;
    updateMemorySlide();
}

function prevSlide(){
    const memory = memories[activeMemoryIndex];
    activeSlideIndex = (activeSlideIndex - 1 + memory.slides.length) % memory.slides.length;
    updateMemorySlide();
    startMemoryTimer();
}

